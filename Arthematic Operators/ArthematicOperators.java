public class ArthematicOperators {
    public static void main(String args[])
    {   int a,b;
        a=10; b=10;
        System.out.printf("after adding both numbers result is : %d",a+b);
        System.out.printf("\nafter subtracting both numbers result is : %d",a-b);
        System.out.printf("\nafter multipling both numbers result is : %d",a*b);
        System.out.printf("\nafter divide both numbers result is : %d",a/b);
        System.out.printf("\nremainder after dividing both numbers is : %d",a%b);
    }
}
